    <p><b>Government hotline</b> –
        <small class="text-muted">a mechanism for communication between the Government and citizens allowing to
            fast-track solving problem issues raised by citizens in their appeals, as well as improve the work of
            executive authorities in the light of public opinion. </small></p>
    <p>Phone calls to the Government hotline 1545 via fixed-line network Ukrtelecom, mobile operators Kyivstar,
        Vodafone Ukraine, Lifecell are free.</p>

    <p>For Ukraine’s citizens abroad phone number of the Government hotline is
        <a href="tel:+38 044 284 19 15">+38 044 284 19 15</a>
        (according to the rates of telecoms provider).</p>

    <small class="text-muted">Electronic appeal to the executive authorities is available to submit
        through the website of the Government Contact Center: <a href="https://www.ukc.gov.ua">www.ukc.gov.ua</a>
    </small>
